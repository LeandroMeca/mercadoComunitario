    public class Daemon implements Runnable {

        JProgressBar dd;
        int bar = 0;

        public Daemon(JProgressBar progress) {
            dd = progress;
        }

        protected volatile boolean runnable = false;

        @Override
        public void run() {
            synchronized (this) {
                try {
                    for (int i=0; i<=100; i++) {
                        Thread.sleep(1);
                        bar = i;
                        dd.setValue(bar);

                    }
                } catch (InterruptedException ex) {

                }
            }
        }

    }

